				<div class="col-sm-8 col-sm-pull-4 text_content">
					<p>Club Lusitano was opened on the 17th December 1866 and the unveiling of the building at that time was regarded as one of the most important occasions for the Portuguese community of Hong Kong. The property, located on Shelley Street, Mid-levels, was spacious and impressive in the style of the best Neo-Classical architecture. It had a huge salon, a theatre, large library and a variety of social rooms and all theatrical entertainment in Hong Kong was held there before the City Hall Theatre was opened in 1869. On the top two floors rooms were available for rent by members by the month or by the day for visitors from Macau. </p>
					<p>The Club Lusitano quickly became the heart of the small Hong Kong Portuguese community and all of the important official, cultural and family ceremonies were marked at the club. Shelly Street was a good club location for the Portuguese community of the mid-19th century. In the steep streets and lanes in and around Caine Road and Robinson Road lived many Portuguese who had relocated from Macau in search of better economic opportunities in the quickly expanding British colony. The club was therefore within easy walking distance of their homes and many availed themselves of the facilities on a daily basis. This part of Mid-Levels was known by the Portuguese as Mato Morro or “field/ wood of the Moors”, because of its Muslim community situated around the then Lascar Temple (1850) now Jamia Mosque in Mosque Street. </p>
					<p>The new Portuguese settlers provided a competent workforce with vital local knowledge and language skills for the British trading companies, military and colonial government. However, there was little or no colonial government support and the Portuguese community largely funded the Club Lusitano from its own resources. They also made significant contributions towards the establishment of many of the early Catholic institutions in Hong Kong such as the Canossian Mission (1850), Sacred Heart Canossian College (1860), St Joseph’s College (1875) and the Catholic Cathedral (1888).  Some three quarters of the construction cost of the first Club Lusitano has been attributed to the generosity of two prominent members Mr. J. A. Baretto and Mr. Delfino Noronha.</p>
					<p>On completion, the Club Lusitano with its fine gala balls, theatrical performances and official ceremonies contributed greatly to integrating the Portuguese community into the British colony and giving it status in the early days of Hong Kong. It was much more than a community center, more a place where the Portuguese could socialize with members of other communities including many members of the colonial establishment. Here they could share their experiences, entertain their guests, and discuss local and world affairs.  Many dignitaries were received including frequent visits from the Governors of both Hong Kong and Macau, military personnel and overseas delegations.</p>
					<p>In an extract from “Twentieth Century Impressions of Hong Kong, Shanghai, and Other Treaty Ports of China.” Lloyds London (1908), the Club Lusitano was described as:-</p>
					<p class="highlight-text">This  Club, the membership of which is confined to the Portuguese, was founded some forty years ago, and is consequently one of the oldest social institutions in the Colony. A limited number of debentures ($75 each) are held by the members, who have to pay an entrance fee of $5, and a monthly subscription of $3. The Club passed through various vicissitudes, but now, largely owing to the efforts of Mr. F. J. V. Jorge and other friends and supporters of the institution, it is in a sound condition. The premises in Shelley Street were specially erected to serve the purposes of a club, and are, therefore, very conveniently arranged. The billiard room contains four tables, and the library, the "Bibliotheca Lusitana," stocked with some ten thousand volumes, chiefly Portuguese literature, is one of the most extensive in the Far East. A spacious ballroom is often used for the presentation of amateur theatricals, for which the Club members have gained quite a high reputation, and there are also several rooms for residential purposes. Mr. F. J. V. Jorge is president of the Club, which is managed by a committee of six directors and a salaried secretary.</p>
					<p>Amongst the enduring legacies from this period is the Lusitano Cup, an annual prestigious horse race that was first held in 1863 at Happy Valley. It is symbolic of the Portuguese community’s long association with horse racing in Hong Kong and it continues to this day with the annual Lusitano Challenge Cup, a class 3 race over 1400m with a HK$1 million purse.</p>
