					<p>Many unique dishes resulted from the spice blends that the wives of Portuguese sailors used in an attempt to replicate European dishes. Its ingredients and seasonings include those from Europe, Latin America, Africa, India, and Southeast Asia, as well as local Chinese ingredients.</p>
					<p>Common cooking techniques include baking, grilling and roasting. The former, seldom seen in other styles of Chinese cooking, speaks to the eclectic nature of Macanese cooking. Macau is renowned for its flavour-blending culture, and modern Macanese cuisine may be considered a type of fusion cuisine.</p>
					<p>Typically, Macanese food is seasoned with various spices including turmeric, coconut milk, cinnamon and bacalhau, giving special aromas and tastes. Famous dishes include Galinha a Portuguesa, Galinha a Africana (African chicken), Bacalhau, Pato de cabidela, Macanese chili shrimps, Minchi,[1][2][3] and stir-fried curry crab. Other dishes include pig's ear and papaya salad, and rabbit stewed in wine, cinnamon and star anise. Tapas are also an integral part of Macanese cuisine.</p>
